/**
 * Busca coordenadas para uma cidade brasileira.
 * Retorna [lat, lng] ou null se não encontrada.
 */
export function buscarCoordenadas(cidade: string, uf: string): [number, number] | null {
  if (!cidade || !uf) return null;
  const key = `${n(cidade)}|${uf.toUpperCase()}`;
  return COORDS.get(key) ?? null;
}
