/** Normaliza nome de cidade para lookup: remove acentos, lowercase, trim */
function n(s: string): string {
  return s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[''`]/g, "")
    .trim();
}

const COORDS = new Map<string, [number, number]>();

function add(nome: string, uf: string, lat: number, lng: number) {
  COORDS.set(`${n(nome)}|${uf.toUpperCase()}`, [lat, lng]);
}

